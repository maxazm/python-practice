fruits = ["orange", "banana", "apple"]
print(fruits)
# .append
fruits.append("grapes")
print(fruits)
# .insert
fruits.insert(2, "melon")
print(fruits)
# .remove
fruits.remove("apple")
print(fruits)
# .sort
fruits.sort()
print(fruits)
fruits.sort(reverse=True)
print(fruits)
# .len()
print(len(fruits))

